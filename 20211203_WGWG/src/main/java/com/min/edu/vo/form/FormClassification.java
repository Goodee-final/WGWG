package com.min.edu.vo.form;

public class FormClassification {

	private int form_class_no;
	private String form_class_nm;
	
	public int getForm_class_no() {
		return form_class_no;
	}
	public void setForm_class_no(int form_class_no) {
		this.form_class_no = form_class_no;
	}
	public String getForm_class_nm() {
		return form_class_nm;
	}
	public void setForm_class_nm(String form_class_nm) {
		this.form_class_nm = form_class_nm;
	}
	
	@Override
	public String toString() {
		return "FormClassification [form_class_no=" + form_class_no + ", form_class_nm=" + form_class_nm + "]";
	}
}
