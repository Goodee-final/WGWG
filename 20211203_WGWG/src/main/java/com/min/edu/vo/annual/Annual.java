package com.min.edu.vo.annual;

public class Annual {

	private int emp_no;
	private int total_annual;
	private int used_annual;
	
	public int getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}
	public int getTotal_annual() {
		return total_annual;
	}
	public void setTotal_annual(int total_annual) {
		this.total_annual = total_annual;
	}
	public int getUsed_annual() {
		return used_annual;
	}
	public void setUsed_annual(int used_annual) {
		this.used_annual = used_annual;
	}
	
	
}
