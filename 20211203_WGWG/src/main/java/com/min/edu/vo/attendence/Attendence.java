package com.min.edu.vo.attendence;

public class Attendence {

	private int attendance_no;
	private String attendance_mo_st_time;
	private String attendance_mo_ed_time;
	private String attendance_mo_reg_dt;
	private String attendance_mo_reason;
	private String attendance_mo_chk;
	private int emp_no;
	
	public int getAttendance_no() {
		return attendance_no;
	}
	public void setAttendance_no(int attendance_no) {
		this.attendance_no = attendance_no;
	}
	public String getAttendance_mo_st_time() {
		return attendance_mo_st_time;
	}
	public void setAttendance_mo_st_time(String attendance_mo_st_time) {
		this.attendance_mo_st_time = attendance_mo_st_time;
	}
	public String getAttendance_mo_ed_time() {
		return attendance_mo_ed_time;
	}
	public void setAttendance_mo_ed_time(String attendance_mo_ed_time) {
		this.attendance_mo_ed_time = attendance_mo_ed_time;
	}
	public String getAttendance_mo_reg_dt() {
		return attendance_mo_reg_dt;
	}
	public void setAttendance_mo_reg_dt(String attendance_mo_reg_dt) {
		this.attendance_mo_reg_dt = attendance_mo_reg_dt;
	}
	public String getAttendance_mo_reason() {
		return attendance_mo_reason;
	}
	public void setAttendance_mo_reason(String attendance_mo_reason) {
		this.attendance_mo_reason = attendance_mo_reason;
	}
	public String getAttendance_mo_chk() {
		return attendance_mo_chk;
	}
	public void setAttendance_mo_chk(String attendance_mo_chk) {
		this.attendance_mo_chk = attendance_mo_chk;
	}
	public int getEmp_no() {
		return emp_no;
	}
	public void setEmp_no(int emp_no) {
		this.emp_no = emp_no;
	}
	
	
}
